for A in range(1, 10):                              #for문 A에 1씩 10번 증가
    A>=1                                            #1과 같거나 큰 A
    for i in range(1, 10):                          #for문 i에 1씩 10번 증가
        print("%a x %a = %a" %(A, i, i*A))          #출력문에 변수 "%a"에 값을 %()에 순차적으로 작성
        print()                                     #공백
